
function add(num1, num2) {
  return num1 + num2;
}

function substract(num1, num2) {
  return num1 - num2;
}

function multiplies(num1, num2) {
  return num1 * num2;
}

function divide(num1, num2) {
  return num1 / num2;
}


module.exports = {
  add: add,
  substract: substract,
  multiplies: multiplies,
  divide: divide
};
